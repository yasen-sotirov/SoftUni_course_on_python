product = input()

if product == 'banana' or product == 'apple' or product == 'cherry' or product == 'lemon' or product == 'grapes' \
        or product == 'kiwi':
    print('fruit')
elif product == 'tomato' or product == 'cucumber' or product == 'pepper' or product == 'carrot':
    print('vegetable')
else:
    print('unknown')

